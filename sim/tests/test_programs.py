class TestTB06_Jumps(unittest.TestCase):

    def setUp(self):
        m = {}
        # Bloque0 @ 0x0000: INC [0x0100]; JP 0x0010
        m[0x0000]=0x13; m[0x0001]=0x00; m[0x0002]=0x01   # LD A,[0x0100]
        m[0x0003]=0xC2                                     # INC A
        m[0x0004]=0x31; m[0x0005]=0x00; m[0x0006]=0x01   # ST A,[0x0100]
        m[0x0007]=0x70; m[0x0008]=0x10; m[0x0009]=0x00   # JP 0x0010
        # Bloque1 @ 0x0010: INC [0x0101]; JR +7 → 0x0020
        m[0x0010]=0x13; m[0x0011]=0x01; m[0x0012]=0x01
        m[0x0013]=0xC2
        m[0x0014]=0x31; m[0x0015]=0x01; m[0x0016]=0x01
        m[0x0017]=0x71; m[0x0018]=0x07                    # JR +7 → (0x0019+7=0x0020)
        # Bloque2 @ 0x0020: INC [0x0102]; JPN 0x30 → 0x0030
        m[0x0020]=0x13; m[0x0021]=0x02; m[0x0022]=0x01
        m[0x0023]=0xC2
        m[0x0024]=0x31; m[0x0025]=0x02; m[0x0026]=0x01
        m[0x0027]=0x72; m[0x0028]=0x30                    # JPN 0x30 → 0x0030
        # Bloque3 @ 0x0030: INC [0x0103]; LD A,#0 / LD B,#0x40; JP A:B → 0x0040
        m[0x0030]=0x13; m[0x0031]=0x03; m[0x0032]=0x01
        m[0x0033]=0xC2
        m[0x0034]=0x31; m[0x0035]=0x03; m[0x0036]=0x01
        m[0x0037]=0x11; m[0x0038]=0x00                    # LD A,#0x00
        m[0x0039]=0x21; m[0x003A]=0x40                    # LD B,#0x40
        m[0x003B]=0x74                                     # JP A:B → 0x0040
        # Bloque4 @ 0x0040: INC [0x0104]; HALT
        m[0x0040]=0x13; m[0x0041]=0x04; m[0x0042]=0x01
        m[0x0043]=0xC2
        m[0x0044]=0x31; m[0x0045]=0x04; m[0x0046]=0x01
        m[0x0047]=0x01                                     # HALT
        self.cpu = make_cpu(m)
        run_to_halt(self.cpu)

