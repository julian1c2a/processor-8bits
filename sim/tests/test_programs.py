class TestTB01_Unary(unittest.TestCase):
    """
    Inicio: A=0xFE, B=0x05
    Operaciones: INC A ×2, DEC A ×2, NOT, NEG, CLR, SET, INC B, DEC B, SWAP A
    Resultados en RAM[0x0100..0x010A]
    """

    def setUp(self):
        m = {}
        # LD A,#0xFE
        m[0x0000]=0x11; m[0x0001]=0xFE
        # LD B,#0x05
        m[0x0002]=0x21; m[0x0003]=0x05
        # INC A → 0xFF → ST A,[0x0100]
        m[0x0004]=0xC2
        m[0x0005]=0x31; m[0x0006]=0x00; m[0x0007]=0x01
        # INC A → 0x00 → ST A,[0x0101]
        m[0x0008]=0xC2
        m[0x0009]=0x31; m[0x000A]=0x01; m[0x000B]=0x01
        # DEC A → 0xFF → ST A,[0x0102]
        m[0x000C]=0xC3
        m[0x000D]=0x31; m[0x000E]=0x02; m[0x000F]=0x01
        # DEC A → 0xFE → ST A,[0x0103]
        m[0x0010]=0xC3
        m[0x0011]=0x31; m[0x0012]=0x03; m[0x0013]=0x01
        # NOT A (0xFE→0x01) → ST A,[0x0104]
        m[0x0014]=0xC0
        m[0x0015]=0x31; m[0x0016]=0x04; m[0x0017]=0x01
        # NEG A (0x01→0xFF) → ST A,[0x0105]
        m[0x0018]=0xC1
        m[0x0019]=0x31; m[0x001A]=0x05; m[0x001B]=0x01
        # CLR A → 0x00 → ST A,[0x0106]
        m[0x001C]=0xC6
        m[0x001D]=0x31; m[0x001E]=0x06; m[0x001F]=0x01
        # SET A → 0xFF → ST A,[0x0107]
        m[0x0020]=0xC7
        m[0x0021]=0x31; m[0x0022]=0x07; m[0x0023]=0x01
        # INC B (0x05→0x06) → LD A,B → ST A,[0x0108]
        m[0x0024]=0xC4
        m[0x0025]=0x10
        m[0x0026]=0x31; m[0x0027]=0x08; m[0x0028]=0x01
        # DEC B (0x06→0x05) → LD A,B → ST A,[0x0109]
        m[0x0029]=0xC5
        m[0x002A]=0x10
        m[0x002B]=0x31; m[0x002C]=0x09; m[0x002D]=0x01
        # LD A,#0x0F → SWAP A (0x0F→0xF0) → ST A,[0x010A]
        m[0x002E]=0x11; m[0x002F]=0x0F
        m[0x0030]=0xCE
        m[0x0031]=0x31; m[0x0032]=0x0A; m[0x0033]=0x01
        # HALT
        m[0x0034]=0x01
        self.cpu = make_cpu(m)
        run_to_halt(self.cpu)

    def test_inc_a_wrap_up(self):       self.assertEqual(self.cpu.mem[0x0100], 0xFF)
    def test_inc_a_wrap_zero(self):     self.assertEqual(self.cpu.mem[0x0101], 0x00)
    def test_dec_a_wrap_ff(self):       self.assertEqual(self.cpu.mem[0x0102], 0xFF)
    def test_dec_a(self):               self.assertEqual(self.cpu.mem[0x0103], 0xFE)
    def test_not_a(self):               self.assertEqual(self.cpu.mem[0x0104], 0x01)
    def test_neg_a(self):               self.assertEqual(self.cpu.mem[0x0105], 0xFF)
    def test_clr_a(self):               self.assertEqual(self.cpu.mem[0x0106], 0x00)
    def test_set_a(self):               self.assertEqual(self.cpu.mem[0x0107], 0xFF)
    def test_inc_b(self):               self.assertEqual(self.cpu.mem[0x0108], 0x06)
    def test_dec_b(self):               self.assertEqual(self.cpu.mem[0x0109], 0x05)
    def test_swap_a(self):              self.assertEqual(self.cpu.mem[0x010A], 0xF0)


