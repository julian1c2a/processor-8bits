class TestTB12_Interrupts(unittest.TestCase):

    def setUp(self):
        m = {}
        # Vectores
        m[0xFFFE]=0x50; m[0xFFFF]=0x00   # IRQ → 0x0050
        m[0xFFFA]=0x60; m[0xFFFB]=0x00   # NMI → 0x0060
        # Programa: LD SP,#0x01FE; SEI; HALT; HALT; HALT
        m[0x0000]=0x50; m[0x0001]=0xFF; m[0x0002]=0x01   # LD SP,#0x01FF (→ 0x01FE)
        m[0x0003]=0x04                                     # SEI
        m[0x0004]=0x01                                     # HALT  ← espera IRQ
        m[0x0005]=0x01                                     # HALT  ← espera NMI
        m[0x0006]=0x01                                     # HALT  ← fin
        # Handler IRQ @ 0x0050: INC [0x0100]; RTI
        m[0x0050]=0x13; m[0x0051]=0x00; m[0x0052]=0x01
        m[0x0053]=0xC2
        m[0x0054]=0x31; m[0x0055]=0x00; m[0x0056]=0x01
        m[0x0057]=0x06                                     # RTI
        # Handler NMI @ 0x0060: INC [0x0101]; RTI
        m[0x0060]=0x13; m[0x0061]=0x01; m[0x0062]=0x01
        m[0x0063]=0xC2
        m[0x0064]=0x31; m[0x0065]=0x01; m[0x0066]=0x01
        m[0x0067]=0x06                                     # RTI
        cpu = make_cpu(m)
        # Ejecutar hasta primer HALT
        run_to_halt(cpu)
        # Inyectar IRQ; ejecutar hasta HALT tras RTI
        cpu.request_irq()
        run_to_halt(cpu)
        # Inyectar NMI; ejecutar hasta HALT tras RTI
        cpu.request_nmi()
        run_to_halt(cpu)
        self.cpu = cpu

    def test_irq_handler_ran(self): self.assertEqual(self.cpu.mem[0x0100], 1)
    def test_nmi_handler_ran(self): self.assertEqual(self.cpu.mem[0x0101], 1)


