class TestTB04_Shifts(unittest.TestCase):

    def setUp(self):
        m = {}
        # LSL 0xAA → 0x54
        m[0x0000]=0x11; m[0x0001]=0xAA
        m[0x0002]=0x03                     # CLC
        m[0x0003]=0xC8                     # LSL A
        m[0x0004]=0x31; m[0x0005]=0x00; m[0x0006]=0x01
        # LSR 0xAA → 0x55
        m[0x0007]=0x11; m[0x0008]=0xAA
        m[0x0009]=0xC9                     # LSR A
        m[0x000A]=0x31; m[0x000B]=0x01; m[0x000C]=0x01
        # ASL 0xAA → 0x54
        m[0x000D]=0x11; m[0x000E]=0xAA
        m[0x000F]=0xCA                     # ASL A
        m[0x0010]=0x31; m[0x0011]=0x02; m[0x0012]=0x01
        # ASR 0xAA → 0xD5
        m[0x0013]=0x11; m[0x0014]=0xAA
        m[0x0015]=0xCB                     # ASR A
        m[0x0016]=0x31; m[0x0017]=0x03; m[0x0018]=0x01
        # ROL 0xAA con C=0 → 0x54
        m[0x0019]=0x11; m[0x001A]=0xAA
        m[0x001B]=0x03                     # CLC
        m[0x001C]=0xCC                     # ROL A
        m[0x001D]=0x31; m[0x001E]=0x04; m[0x001F]=0x01
        # ROR 0xAA con C=0 → 0x55
        m[0x0020]=0x11; m[0x0021]=0xAA
        m[0x0022]=0x03                     # CLC
        m[0x0023]=0xCD                     # ROR A
        m[0x0024]=0x31; m[0x0025]=0x05; m[0x0026]=0x01
        # ROL 0x01 con C=1 → 0x03
        m[0x0027]=0x11; m[0x0028]=0x01
        m[0x0029]=0x02                     # SEC
        m[0x002A]=0xCC                     # ROL A
        m[0x002B]=0x31; m[0x002C]=0x06; m[0x002D]=0x01
        # ROR 0x80 con C=1 → 0xC0
        m[0x002E]=0x11; m[0x002F]=0x80
        m[0x0030]=0x02                     # SEC
        m[0x0031]=0xCD                     # ROR A
        m[0x0032]=0x31; m[0x0033]=0x07; m[0x0034]=0x01
        # HALT
        m[0x0035]=0x01
        self.cpu = make_cpu(m)
        run_to_halt(self.cpu)

    def test_lsl(self):       self.assertEqual(self.cpu.mem[0x0100], 0x54)
    def test_lsr(self):       self.assertEqual(self.cpu.mem[0x0101], 0x55)
    def test_asl(self):       self.assertEqual(self.cpu.mem[0x0102], 0x54)
    def test_asr(self):       self.assertEqual(self.cpu.mem[0x0103], 0xD5)
    def test_rol_c0(self):    self.assertEqual(self.cpu.mem[0x0104], 0x54)
    def test_ror_c0(self):    self.assertEqual(self.cpu.mem[0x0105], 0x55)
    def test_rol_c1(self):    self.assertEqual(self.cpu.mem[0x0106], 0x03)
