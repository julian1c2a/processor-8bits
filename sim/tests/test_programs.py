class TestTB02_ALUReg(unittest.TestCase):

    def setUp(self):
        m = {}
        # ADD: 0x07+0x08=0x0F
        m[0x0000]=0x11; m[0x0001]=0x07
        m[0x0002]=0x21; m[0x0003]=0x08
        m[0x0004]=0x90
        m[0x0005]=0x31; m[0x0006]=0x00; m[0x0007]=0x01
        # ADC: 0x07+0x08+C=1=0x10 (SEC primero)
        m[0x0008]=0x11; m[0x0009]=0x07
        m[0x000A]=0x21; m[0x000B]=0x08
        m[0x000C]=0x02                     # SEC
        m[0x000D]=0x91
        m[0x000E]=0x31; m[0x000F]=0x01; m[0x0010]=0x01
        # SUB: 0x07-0x08=0xFF (borrow)
        m[0x0011]=0x11; m[0x0012]=0x07
        m[0x0013]=0x21; m[0x0014]=0x08
        m[0x0015]=0x92
        m[0x0016]=0x31; m[0x0017]=0x02; m[0x0018]=0x01
        # SBB: 0x07-0x08-C=1=0xFE (SEC primero)
        m[0x0019]=0x11; m[0x001A]=0x07
        m[0x001B]=0x21; m[0x001C]=0x08
        m[0x001D]=0x02                     # SEC
        m[0x001E]=0x93
        m[0x001F]=0x31; m[0x0020]=0x03; m[0x0021]=0x01
        # AND: 0x0F & 0x35=0x05
        m[0x0022]=0x11; m[0x0023]=0x0F
        m[0x0024]=0x21; m[0x0025]=0x35
        m[0x0026]=0x94
        m[0x0027]=0x31; m[0x0028]=0x04; m[0x0029]=0x01
        # OR: 0x0F | 0x35=0x3F
        m[0x002A]=0x11; m[0x002B]=0x0F
        m[0x002C]=0x21; m[0x002D]=0x35
        m[0x002E]=0x95
        m[0x002F]=0x31; m[0x0030]=0x05; m[0x0031]=0x01
        # XOR: 0x0F ^ 0x35=0x3A
        m[0x0032]=0x11; m[0x0033]=0x0F
        m[0x0034]=0x21; m[0x0035]=0x35
        m[0x0036]=0x96
        m[0x0037]=0x31; m[0x0038]=0x06; m[0x0039]=0x01
        # CMP: A no cambia → 0x07
        m[0x003A]=0x11; m[0x003B]=0x07
        m[0x003C]=0x21; m[0x003D]=0x05
        m[0x003E]=0x97
        m[0x003F]=0x31; m[0x0040]=0x07; m[0x0041]=0x01
        # MUL: 0x07*0x08=0x38 (byte bajo)
        m[0x0042]=0x11; m[0x0043]=0x07
        m[0x0044]=0x21; m[0x0045]=0x08
        m[0x0046]=0x98
        m[0x0047]=0x31; m[0x0048]=0x08; m[0x0049]=0x01
        # MUH: byte alto del mismo producto=0x00
        m[0x004A]=0x11; m[0x004B]=0x07
        m[0x004C]=0x21; m[0x004D]=0x08
        m[0x004E]=0x99
        m[0x004F]=0x31; m[0x0050]=0x09; m[0x0051]=0x01
        # HALT
        m[0x0052]=0x01
        self.cpu = make_cpu(m)
        run_to_halt(self.cpu)

    def test_add(self): self.assertEqual(self.cpu.mem[0x0100], 0x0F)
    def test_adc(self): self.assertEqual(self.cpu.mem[0x0101], 0x10)
    def test_sub(self): self.assertEqual(self.cpu.mem[0x0102], 0xFF)
    def test_sbb(self): self.assertEqual(self.cpu.mem[0x0103], 0xFE)
    def test_and(self): self.assertEqual(self.cpu.mem[0x0104], 0x05)
    def test_or(self):  self.assertEqual(self.cpu.mem[0x0105], 0x3F)
    def test_xor(self): self.assertEqual(self.cpu.mem[0x0106], 0x3A)
    def test_cmp(self): self.assertEqual(self.cpu.mem[0x0107], 0x07)
    def test_mul(self): self.assertEqual(self.cpu.mem[0x0108], 0x38)
    def test_muh(self): self.assertEqual(self.cpu.mem[0x0109], 0x00)


