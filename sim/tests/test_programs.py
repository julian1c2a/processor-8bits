# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def make_cpu(byte_map: dict) -> CPU:
    """Crea CPU y carga los bytes del diccionario {addr: value}."""
    c = CPU()
    for addr, val in byte_map.items():
        c.mem[addr & 0xFFFF] = val & 0xFF
    return c


def run_to_halt(cpu: CPU, max_steps: int = 5000) -> CPU:
    """
    Ejecuta el CPU hasta que se detenga (HALT) o se agoten los pasos.

    Si el CPU está en HALT con una interrupción pendiente, ejecuta un paso
    para procesarla (puede deshacer el HALT) y continúa.
    Si el CPU permanece en HALT sin interrupciones pendientes, retorna.
    """
    for _ in range(max_steps):
        if cpu.halted:
            r = cpu.step()   # intenta procesar interrupción pendiente
            if cpu.halted:   # sigue halted → no había interrupción, fin
                return cpu
            # Si ya no está halted → interrupción procesada, continúa
        else:
            cpu.step()
    raise AssertionError(
        f'CPU no alcanzó HALT en {max_steps} pasos '
        f'(PC={cpu.PC:#06x}, halted={cpu.halted})'
    )


